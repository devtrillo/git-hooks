exec(
  "ln -sfv $(pwd)/dist/post-checkout ~/.config/git-templates/hooks/post-checkout",
  (error, stdout, stderr) => {
    if (error) {
      console.log(`error: ${error.message}`);
      return;
    }
    if (stderr) {
      console.log(`stderr: ${stderr}`);
      return;
    }
    console.log(`stdout: ${stdout}`);
  }
);
